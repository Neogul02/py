# 파이썬에서 int와 float는 숫자를 다루기 위한 데이터 타입입니다.

# int는 정수를 나타내며, float는 부동소수점 숫자를 나타냅니다. 
# 정수는 0, 양의 정수, 음의 정수 등을 포함하는 반면에, 부동소수점 숫자는 소수점을 가지고 있으며, 
# 지수 표기법으로 나타낼 수 있는 아주 큰 숫자나 아주 작은 숫자를 나타낼 수 있습니다.

# 정수
a = 5
b = -3
c = 0

# 부동소수점 숫자
x = 3.14
y = -0.5
z = 1.23e-4

# 형변환
i = int(x)  # 부동소수점 숫자를 정수로 변환
f = float(a)  # 정수를 부동소수점 숫자로 변환

# 연산
result1 = a + b  # 정수와 정수의 덧셈
result2 = x * y  # 부동소수점 숫자와 부동소수점 숫자의 곱셈
result3 = a + x  # 정수와 부동소수점 숫자의 덧셈 (자동 형변환)