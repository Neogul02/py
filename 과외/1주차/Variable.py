# 변수에 값 할당
x = 1
y = 2
z = x + y

# 변수 출력
print("z =", z)  # "z = 3" 출력

# 변수에 새로운 값 할당
x = 3
z = x + y

# 변수 출력
print("z =", z)  # "z = 5" 출력

# 파이썬에서는 변수의 타입을 명시적으로 선언하지 않습니다. 
# 대신 변수에 값을 할당하면 그 값의 타입에 따라 자동으로 변수의 타입이 결정됩니다. 
# 예를 들어, 다음과 같이 변수에 문자열과 숫자를 할당할 수 있습니다.

name = "Alice"  # 문자열 타입의 변수
age = 25  # 정수 타입의 변수
height = 1.7  # 실수 타입의 변수

# 변수를 사용할 때, 변수가 선언되어 있지 않으면 NameError가 발생합니다. 
# 또한, 변수의 값을 참조하기 전에 반드시 먼저 변수에 값을 할당해야 합니다.