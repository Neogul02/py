# 파이썬에서는 print() 함수를 사용하여 출력을 수행합니다. 
# print() 함수는 괄호 안에 출력하고자 하는 값을 넣으면 됩니다.

# 문자열 출력
print("Hello, world!")  # "Hello, world!" 출력

# 변수 출력
name = "Alice"
print("My name is", name)  # "My name is Alice" 출력

# 숫자 출력
x = 3
y = 2.5
print(x, y)  # "3 2.5" 출력

# 포맷 문자열
age = 27
print("I am {} years old.".format(age))  # "I am 27 years old." 출력